using System;
using System.Collections.Generic;
using System.Linq;
using System.Web;

namespace FruitStore.Model
{
    public class Fruits
    {
        private string fruitName;

        public string FruitName
        {
            get { return fruitName; }
            set { fruitName = value; }
        }
        private int fruitGroupId;

        public int FruitGroupId
        {
            get { return fruitGroupId; }
            set { fruitGroupId = value; }
        }
        private string fruitImage;

        public string FruitImage
        {
            get { return fruitImage; }
            set { fruitImage = value; }
        }
        private string fruitComment;

        public string FruitComment
        {
            get { return fruitComment; }
            set { fruitComment = value; }
        }
        private float fruitOPrice;

        public float FruitOPrice
        {
            get { return fruitOPrice; }
            set { fruitOPrice = value; }
        }
        private float fruitNPrice;

        public float FruitNPrice
        {
            get { return fruitNPrice; }
            set { fruitNPrice = value; }
        }
        public Fruits(string name, int gid, string image = null, string comment = null, float op = 0, float np = 0)
        {
            FruitName = name;
            FruitGroupId = gid;
            FruitImage = image;
            FruitComment = comment;
            FruitOPrice = op;
            fruitNPrice = np;
        }
    }
}